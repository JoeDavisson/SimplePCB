public class Pad
{
  double x;
  double y;
  double innerSize;
  double outerSize;
  boolean status = false;

  Pad(double temp_x, double temp_y, double temp_innerSize, double temp_outerSize)
  {
    x = temp_x;
    y = temp_y;
    innerSize = temp_innerSize;
    outerSize = temp_outerSize;
    status = false;
  }

}

